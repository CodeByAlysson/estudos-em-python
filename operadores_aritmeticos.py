# podemos utilizar operadores aritméticos para realizar 
# cálculos matemáticos básicos en Python.

x = 10
y = 3

soma = x + y          # Adição
subtracao = x - y     # Subtração
multiplicacao = x * y  # Multiplicação
divisao = x / y       # Divisão (com resultado float)
divisao_inteira = x // y  # Divisão inteira (resultado inteiro)
resto_divisao = x % y  # Resto da divisão (módulo) 
exponenciacao = x ** y  # Exponenciação
print("Soma:", soma)
print("Subtração:", subtracao)
print("Multiplicação:", multiplicacao)
print("Divisão:", divisao)
print("Divisão Inteira:", divisao_inteira)
print("Resto da Divisão:", resto_divisao)
print("Exponenciação:", exponenciacao)

# Podemos também combinar esses operadores para realizar cálculos mais complexos.

resultado = (x + y) * (x - y) / y ** 2
print("Resultado:", resultado)

